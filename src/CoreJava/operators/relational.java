package CoreJava.operators;

public class relational {
    public static void main(String[] args) {
        int p = 25;
        int q = 78;

        System.out.println(p == q);
        System.out.println(p != q);
        System.out.println(p<= q);
        System.out.println(p >= q);
        System.out.println(p < q);
        System.out.println(p > q);


    }
}
